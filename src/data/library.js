// --- Mapper: API item -> library item ---
function mapApiPlantToLibraryItem(item) {
  const width = Math.max(1, Math.round(item?.width || 1));
  const length = Math.max(1, Math.round(item?.length || 1));
  const desc = item?.plant?.description || "";
  const photos = [item?.plant?.photo_1, item?.plant?.photo_2, item?.plant?.photo_3].filter(Boolean);
  return {
    id: `plant-${item.id}`,
    type: "plant",
    name: item?.name || item?.plant?.name || "Plant",
    iconKey: "plant",
    size: [width, length],
    description: desc,
    photos,
    imageUrl: item?.plant?.photo_1 || photos[0] || null,
    assetLocation: item?.asset_location || null,
    backendId: item?.id,
    backendPlantId: item?.plant?.id || null,
    // raw API record for future use
    __api: item,
  };
}

